
CREATE DATABASE BOOTCAMP_EX3;
use BOOTCAMP_EX3;

DROP TABLE REGIONS;
DROP TABLE COUNTRIES;
DROP TABLE LOCATIONS;
DROP TABLE EMPLOYEES;
DROP TABLE DEPARTMENTS;
DROP TABLE JOBS;
DROP TABLE JOB_HISTORY;


CREATE TABLE REGIONS (
REGION_ID INTEGER  PRIMARY KEY,
REGION_NAME VARCHAR(25)
);
-- Error Code: 3780. Referencing column 'COUNTRY_ID' and referenced column 'COUNTRY_ID' in foreign key constraint 'locations_ibfk_1' are incompatible.

CREATE TABLE COUNTRIES (
COUNTRY_ID VARCHAR(2) PRIMARY KEY,
COUNTRY_NAME VARCHAR(40),
REGION_ID INTEGER,
FOREIGN KEY (REGION_ID) REFERENCES REGIONS (REGION_ID)
);

CREATE TABLE LOCATIONS (
LOCATION_ID INTEGER  PRIMARY KEY,
STREET_ADDRESS VARCHAR (25),
POSTAL_CODE VARCHAR(12),
CITY VARCHAR(30),
STATE_PROVINCE VARCHAR(12),
COUNTRY_ID VARCHAR(2),
FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRIES (COUNTRY_ID)
);



CREATE TABLE DEPARTMENTS(
DEPARTMENT_ID INTEGER PRIMARY KEY,
DEPARTMENT_NAME VARCHAR(30),
MANAGER_ID INTEGER,
LOCATION_ID INTEGER,
FOREIGN KEY (LOCATION_ID) REFERENCES LOCATIONS (LOCATION_ID)
);


CREATE TABLE EMPLOYESS(
	EMPLOYEE_ID INTEGER PRIMARY KEY, -- UNQIUE, NOT NULL
	FIRST_NAME VARCHAR(20),
    LAST_NAME VARCHAR(25),
	EMAIL VARCHAR(25),
    PHONE_NUMBER VARCHAR (20),
    HIRE_DATE DATE,
    SALARY INTEGER,
    COMMISSION_PCT INTEGER,
    MANAGER_ID INTEGER,
    DEPARTMENT_ID INTEGER,
    JOB_ID VARCHAR(10),
	FOREIGN KEY (DEPARTMENT_ID) REFERENCES  DEPARTMENTS (DEPARTMENT_ID),
    FOREIGN KEY (JOB_ID) REFERENCES JOBS (JOB_ID)
);

CREATE TABLE JOBS( 
 JOB_ID VARCHAR (10) PRIMARY KEY,
 JOB_TITLE VARCHAR(35),
 MIN_SALARY INTEGER,
 MAX_SALARY INTEGER,
 GRADES_LEVEL  VARCHAR(2),
FOREIGN KEY (GRADES_LEVEL ) REFERENCES JOB_GRADES (GRADES_LEVEL )
);
 
 CREATE TABLE JOB_HISTORY(
 FOREIGN KEY (EMPLOYEE_ID ) REFERENCES  EMPLOYESS(EMPLOYEE_ID ),
 EMPLOYEE_ID INTEGER ,
 START_DATE DATE ,
 END_DATE DATE,
 JOB_ID VARCHAR(10),
 DEPARTMENT_ID INTEGER,
 FOREIGN KEY (JOB_ID) REFERENCES JOBS (JOB_ID),
 FOREIGN KEY (DEPARTMENT_ID) REFERENCES DEPARTMENTS (DEPARTMENT_ID),
 PRIMARY KEY (START_DATE, EMPLOYEE_ID)
 );
 
